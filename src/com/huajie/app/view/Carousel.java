package com.huajie.app.view;

public class Carousel {
	private String carousel_url;
	private String carousel_page_url;
	private String carousel_instruction;
	
	public String getCarousel_url() {
		return carousel_url;
	}
	public void setCarousel_url(String carousel_url) {
		this.carousel_url = carousel_url;
	}
	public String getCarousel_page_url() {
		return carousel_page_url;
	}
	public void setCarousel_page_url(String carousel_page_url) {
		this.carousel_page_url = carousel_page_url;
	}
	public String getCarousel_instruction() {
		return carousel_instruction;
	}
	public void setCarousel_instruction(String carousel_instruction) {
		this.carousel_instruction = carousel_instruction;
	}
}
