package com.huajie.app.view;

public class Practice {
	private String practice_id;
	private String practice_name;
	private String practice_num;
	private String practice_version;
	private String practice_url;
	private String practice_md5;
	private String knowledge_point_id;
	private String practice_errorNum;
	private String done_num;
	
	public String getPractice_id() {
		return practice_id;
	}
	public void setPractice_id(String practice_id) {
		this.practice_id = practice_id;
	}
	public String getPractice_name() {
		return practice_name;
	}
	public void setPractice_name(String practice_name) {
		this.practice_name = practice_name;
	}
	public String getPractice_num() {
		return practice_num;
	}
	public void setPractice_num(String practice_num) {
		this.practice_num = practice_num;
	}
	public String getPractice_version() {
		return practice_version;
	}
	public void setPractice_version(String practice_version) {
		this.practice_version = practice_version;
	}
	public String getPractice_url() {
		return practice_url;
	}
	public void setPractice_url(String practice_url) {
		this.practice_url = practice_url;
	}
	public String getPractice_md5() {
		return practice_md5;
	}
	public void setPractice_md5(String practice_md5) {
		this.practice_md5 = practice_md5;
	}
	public String getKnowledge_point_id() {
		return knowledge_point_id;
	}
	public void setKnowledge_point_id(String knowledge_point_id) {
		this.knowledge_point_id = knowledge_point_id;
	}
	public String getPractice_errorNum() {
		return practice_errorNum;
	}
	public void setPractice_errorNum(String practice_errorNum) {
		this.practice_errorNum = practice_errorNum;
	}
	public String getDone_num() {
		return done_num;
	}
	public void setDone_num(String done_num) {
		this.done_num = done_num;
	}

}
