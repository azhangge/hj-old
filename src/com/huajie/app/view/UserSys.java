package com.huajie.app.view;

public class UserSys {
	private String sysId;
	private String sysName;
	private String sysRootPath;
	private String sysPic;
	private int sort;
	
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	public String getSysId() {
		return sysId;
	}
	public void setSysId(String sysId) {
		this.sysId = sysId;
	}
	public String getSysName() {
		return sysName;
	}
	public void setSysName(String sysName) {
		this.sysName = sysName;
	}
	public String getSysRootPath() {
		return sysRootPath;
	}
	public void setSysRootPath(String sysRootPath) {
		this.sysRootPath = sysRootPath;
	}
	public String getSysPic() {
		return sysPic;
	}
	public void setSysPic(String sysPic) {
		this.sysPic = sysPic;
	}
}
